// a for loop is usually used when repeating a process a known number of times
for (var i = 0; i < 10; i ++){
    console.log(i);
}

// a while loop is usually used when repeating a process until some condition is met
var i = 0
while (i < 10){
    console.log(i);
    i ++;
}

